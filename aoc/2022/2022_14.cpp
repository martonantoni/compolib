int read()
{
    int bottom = 0;
    memset(field, 0, 1024 * 1024);
    cFastFileReader in("in.txt");
    for (auto line : in)
    {
        auto ps = line.toStringVector(", ->", false).ToIntVector();
        for (int i = 2; i < ps.size(); i += 2)
        {
            drawLine(ps[i - 2], ps[i - 1], ps[i], ps[i + 1]);
            bottom = max(bottom, (int)ps[i - 1]);
            bottom = max(bottom, (int)ps[i + 1]);
        }
    }
    return bottom;
}

