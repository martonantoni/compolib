char field[1024 * 1024] = { 0 };

char& at(int x, int y)
{
    return field[x + y * 1024];
}

void drawLine(int from_x, int from_y, int to_x, int to_y)
{
    int dx = from_x == to_x ? 0 : (from_x > to_x ? -1 : 1);
    int dy = from_y == to_y ? 0 : (from_y > to_y ? -1 : 1);
    for (int x = from_x, y = from_y; x != to_x || y != to_y; x += dx, y += dy)
    {
        at(x, y) = '#';
    }
    at(to_x, to_y) = '#';
}

bool oneSand(int start_x, int start_y)
{
    int x = start_x, y = start_y;
    auto check=[&](int dx, int dy)
    {
        if (at(x + dx, y + dy) == 0)
        {
            x += dx;
            y += dy;
            return true;
        }
        return false;
    };
    for (;;)
    {
        if (y == 1000)
            return true;
        if(check(0, 1))
            continue;
        if(check(-1, 1))
            continue;
        if(check(1, 1))
            continue;
        at(x, y) = '*';
        return false;
    }
}

