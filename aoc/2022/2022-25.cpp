pair<char, char> addDigits(char l, char r)
{
    static constexpr array<char, 5> ds = { '=', '-', '0', '1', '2' };
    static constexpr array<int, 5> vs =   { -2,  -1, 0,   1,   2   };
    auto li = find(ALL(ds), l) - ds.begin();
    auto ri = find(ALL(ds), r) - ds.begin();
    auto si = li + vs[ri];
    if (si<0)
    {
        return { '-', ds[si + 5] };
    }
    if (si >= 5)
    {
        return { '1', ds[si - 5] };
    }
    return { '0', ds[si] };
}

string add(string l, string r)
{
    string s;
    reverse(ALL(l));
    reverse(ALL(r));
    char carry = '0';
    for (size_t i = 0, iend = max(l.size(), r.size()); i != iend; ++i)
    {
        auto lc = i < l.size() ? l[i] : '0';
        auto rc = i < r.size() ? r[i] : '0';
        auto [next_carry1, sc1] = addDigits(lc, carry);
        auto [next_carry, sc] = addDigits(sc1, rc);
        carry = addDigits(next_carry, next_carry1).second;
        s.push_back(sc);
    }
    if (carry != '0')
        s.push_back(carry);
    reverse(ALL(s));
    return s;
}

